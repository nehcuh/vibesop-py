"""Tests for ClaudeCodeAdapter."""

import json
from pathlib import Path
from datetime import datetime

from vibesop.adapters import ClaudeCodeAdapter
from vibesop.adapters.models import (
    Manifest,
    ManifestMetadata,
    PolicySet,
    SecurityPolicy,
    RoutingConfig,
)
from vibesop.core.models import SkillDefinition


class TestClaudeCodeAdapter:
    """Test ClaudeCodeAdapter functionality."""

    def test_platform_name(self) -> None:
        """Test platform name."""
        adapter = ClaudeCodeAdapter()
        assert adapter.platform_name == "claude-code"

    def test_config_dir(self) -> None:
        """Test config directory."""
        adapter = ClaudeCodeAdapter()
        assert adapter.config_dir == Path("~/.claude").expanduser()

    def test_get_settings_schema(self) -> None:
        """Test settings schema generation."""
        adapter = ClaudeCodeAdapter()
        schema = adapter.get_settings_schema()

        assert isinstance(schema, dict)
        assert "$schema" in schema
        assert "properties" in schema
        assert "allowedCommands" in schema["properties"]
        assert "permissions" in schema["properties"]

    def test_render_config_basic(self, tmp_path: Path) -> None:
        """Test basic config rendering."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")
        manifest = Manifest(metadata=metadata)

        result = adapter.render_config(manifest, tmp_path)

        assert result.success
        assert result.file_count > 0

        # Check that main files were created
        assert (tmp_path / "CLAUDE.md").exists()
        assert (tmp_path / "rules").exists()
        assert (tmp_path / "docs").exists()
        assert (tmp_path / "skills").exists()
        assert (tmp_path / "settings.json").exists()

    def test_render_config_with_skills(self, tmp_path: Path) -> None:
        """Test rendering with skills."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")

        skill = SkillDefinition(
            id="test-skill",
            name="Test Skill",
            description="A test skill",
            trigger_when="Testing scenario",
        )

        manifest = Manifest(
            metadata=metadata,
            skills=[skill],
        )

        result = adapter.render_config(manifest, tmp_path)

        assert result.success
        assert (tmp_path / "skills" / "test-skill" / "SKILL.md").exists()

    def test_claude_md_content(self, tmp_path: Path) -> None:
        """Test CLAUDE.md content."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(
            platform="claude-code",
            version="1.0.0",
        )
        manifest = Manifest(metadata=metadata)

        result = adapter.render_config(manifest, tmp_path)
        assert result.success

        claude_md = (tmp_path / "CLAUDE.md").read_text()

        # Check for key sections
        assert "# VibeSOP Configuration" in claude_md
        assert "> **Version**: 1.0.0" in claude_md
        assert "> **Platform**: Claude Code" in claude_md
        assert "Generated by VibeSOP" in claude_md

    def test_rules_files_created(self, tmp_path: Path) -> None:
        """Test that all rule files are created."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")
        manifest = Manifest(metadata=metadata)

        result = adapter.render_config(manifest, tmp_path)
        assert result.success

        # Check rule files
        assert (tmp_path / "rules" / "behaviors.md").exists()
        assert (tmp_path / "rules" / "routing.md").exists()
        assert (tmp_path / "rules" / "skill-triggers.md").exists()

        # Check content
        behaviors = (tmp_path / "rules" / "behaviors.md").read_text()
        assert "# Behavior Rules" in behaviors
        assert "Verification Before Claims" in behaviors

    def test_docs_files_created(self, tmp_path: Path) -> None:
        """Test that all doc files are created."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")
        manifest = Manifest(metadata=metadata)

        result = adapter.render_config(manifest, tmp_path)
        assert result.success

        # Check doc files
        assert (tmp_path / "docs" / "safety.md").exists()
        assert (tmp_path / "docs" / "skills.md").exists()
        assert (tmp_path / "docs" / "task-routing.md").exists()

    def test_settings_json(self, tmp_path: Path) -> None:
        """Test settings.json generation."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")
        manifest = Manifest(metadata=metadata)

        result = adapter.render_config(manifest, tmp_path)
        assert result.success

        settings_path = tmp_path / "settings.json"
        assert settings_path.exists()

        settings = json.loads(settings_path.read_text())
        assert isinstance(settings, dict)
        assert "$schema" in settings
        assert "properties" in settings

    def test_render_config_with_custom_policies(self, tmp_path: Path) -> None:
        """Test rendering with custom policies."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")

        security = SecurityPolicy(
            scan_external_content=False,
            max_file_size=1024 * 1024,  # 1MB
        )
        routing = RoutingConfig(
            enable_ai_routing=False,
            confidence_threshold=0.8,
        )

        manifest = Manifest(
            metadata=metadata,
            policies=PolicySet(security=security, routing=routing),
        )

        result = adapter.render_config(manifest, tmp_path)
        assert result.success

        # Check that policies are reflected in output
        claude_md = (tmp_path / "CLAUDE.md").read_text()
        assert "- **Scan External Content**: False" in claude_md
        assert "- **Enable AI Routing**: False" in claude_md

    def test_render_config_invalid_platform(self, tmp_path: Path) -> None:
        """Test rendering with wrong platform."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="wrong-platform")
        manifest = Manifest(metadata=metadata)

        result = adapter.render_config(manifest, tmp_path)

        assert not result.success
        assert len(result.errors) > 0
        assert any("does not match" in e for e in result.errors)

    def test_render_config_multiple_skills(self, tmp_path: Path) -> None:
        """Test rendering with multiple skills."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")

        skills = [
            SkillDefinition(
                id="skill-1",
                name="Skill 1",
                description="First skill",
                trigger_when="Scenario 1",
            ),
            SkillDefinition(
                id="skill-2",
                name="Skill 2",
                description="Second skill",
                trigger_when="Scenario 2",
            ),
        ]

        manifest = Manifest(
            metadata=metadata,
            skills=skills,
        )

        result = adapter.render_config(manifest, tmp_path)
        assert result.success

        # Check that both skills were created
        assert (tmp_path / "skills" / "skill-1" / "SKILL.md").exists()
        assert (tmp_path / "skills" / "skill-2" / "SKILL.md").exists()

        # Check skill content
        skill1_content = (tmp_path / "skills" / "skill-1" / "SKILL.md").read_text()
        assert "# Skill 1" in skill1_content
        assert "First skill" in skill1_content

    def test_install_hooks(self, tmp_path: Path) -> None:
        """Test hook installation."""
        adapter = ClaudeCodeAdapter()

        result = adapter.install_hooks(tmp_path)

        assert isinstance(result, dict)
        assert "pre-session-end" in result

        if result["pre-session-end"]:
            hook_path = tmp_path / "hooks" / "pre-session-end.sh"
            assert hook_path.exists()
            # Check hook is executable
            assert hook_path.stat().st_mode & 0o111 != 0


class TestClaudeCodeAdapterEdgeCases:
    """Test edge cases and error conditions."""

    def test_render_with_empty_skill_list(self, tmp_path: Path) -> None:
        """Test rendering with no skills."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")
        manifest = Manifest(
            metadata=metadata,
            skills=[],
        )

        result = adapter.render_config(manifest, tmp_path)
        assert result.success

        # Should still create skills directory
        assert (tmp_path / "skills").exists()

    def test_render_with_manifest_metadata(self, tmp_path: Path) -> None:
        """Test rendering with full manifest metadata."""
        adapter = ClaudeCodeAdapter()
        now = datetime.now()
        metadata = ManifestMetadata(
            platform="claude-code",
            version="2.0.0",
            author="Test Author",
            description="Test manifest",
            created_at=now,
            updated_at=now,
        )
        manifest = Manifest(metadata=metadata)

        result = adapter.render_config(manifest, tmp_path)
        assert result.success

        claude_md = (tmp_path / "CLAUDE.md").read_text()
        assert "> **Version**: 2.0.0" in claude_md
        assert "**Author**: Test Author" in claude_md
        assert "Test manifest" in claude_md

    def test_template_rendering_error_handling(self, tmp_path: Path) -> None:
        """Test handling of template rendering errors."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")
        manifest = Manifest(metadata=metadata)

        # This should succeed even if templates have issues
        # (Jinja2 will raise errors for missing variables)
        result = adapter.render_config(manifest, tmp_path)

        # If templates are valid, it should succeed
        # If templates are invalid, it should have errors
        assert isinstance(result.success, bool)

    def test_concurrent_render(self, tmp_path: Path) -> None:
        """Test that multiple renders don't interfere."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")
        manifest = Manifest(metadata=metadata)

        # Render twice
        result1 = adapter.render_config(manifest, tmp_path / "config1")
        result2 = adapter.render_config(manifest, tmp_path / "config2")

        assert result1.success
        assert result2.success

        # Both should have created files
        assert (tmp_path / "config1" / "CLAUDE.md").exists()
        assert (tmp_path / "config2" / "CLAUDE.md").exists()

    def test_file_permissions(self, tmp_path: Path) -> None:
        """Test that generated files have correct permissions."""
        adapter = ClaudeCodeAdapter()
        metadata = ManifestMetadata(platform="claude-code")
        manifest = Manifest(metadata=metadata)

        result = adapter.render_config(manifest, tmp_path)
        assert result.success

        # Check that files are readable
        claude_md = tmp_path / "CLAUDE.md"
        assert claude_md.stat().st_mode & 0o444 != 0
